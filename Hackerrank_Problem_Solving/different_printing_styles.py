a="infy"
b=20.127
c=10
print(a,b,c)

# separate the prints by something
print(a,b,c,sep=",")
# print in one line with a space between two data 
print(a,b,c,end=" ")

print("\n")
# print with desired decimal points
print("b=%0.2f" %b)

# print with 8 reserved spaces on right side
print("c=%8d" %c)
# print with 8 reserved spaces on left side
print("c=%-8d" %c)
